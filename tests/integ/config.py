import os
import sys

cfg = {
    'head_host': '127.0.0.1',
    'head_port': 6000
}
   
def get(x):     
    # see if there are an environment variable override
    if x.upper() in os.environ:
        return os.environ[x.upper()]
    # no command line override, just return the cfg value        
    return cfg[x]

  
  
